a = int(input())          # Хэрэглэгчээс нийт минутын тоог авч, бүхэл тоо болгоод 'a' хувьсагчид хадгална
hour = int(a // 60)       # Нийт минутыг 60-д хувааж бүтэн цагийг олно
minut = a % 60            # 60-д хуваасны үлдэгдлээр минутыг олно
print(hour)               # Цагийг хэвлэнэ
print(minut)              # Минутыг хэвлэнэ
